async function compararNomes() {
  try {
    console.log('🔍 Conectando ao banco de dados...');
    
    // Aguardar conexão
    await mongoose.connection.asPromise();
    console.log('✅ Conectado ao MongoDB');
    
    // Buscar todos os clientes importados
    const db = mongoose.connection.db;
    const clientesCollection = db.collection('clients');
    
    console.log('📊 Buscando clientes no banco...');
    const clientesImportados = await clientesCollection.find({}).toArray();
    
    console.log(`📈 Total de clientes no banco: ${clientesImportados.length}`);
    
    // Extrair nomes dos clientes importados
    const nomesImportados = clientesImportados.map(cliente => cliente.name).filter(Boolean);
    
    console.log(`📝 Total de nomes válidos no banco: ${nomesImportados.length}`);
    
    // Comparar nomes
    const nomesEncontrados = [];
    const nomesNaoEncontrados = [];
    
    for (const nome of nomesFornecidos) {
      // Normalizar nome para comparação (remover acentos, maiúsculas, etc.)
      const nomeNormalizado = nome.toLowerCase()
        .normalize('NFD')
        .replace(/[\u0300-\u036f]/g, '')
        .replace(/[^\w\s]/g, '')
        .trim();
      
      // Verificar se existe no banco
      const encontrado = nomesImportados.some(nomeBanco => {
        const nomeBancoNormalizado = nomeBanco.toLowerCase()
          .normalize('NFD')
          .replace(/[\u0300-\u036f]/g, '')
          .replace(/[^\w\s]/g, '')
          .trim();
        
        return nomeBancoNormalizado === nomeNormalizado;
      });
      
      if (encontrado) {
        nomesEncontrados.push(nome);
      } else {
        nomesNaoEncontrados.push(nome);
      }
    }
    
    // Exibir resultados
    console.log('\n📊 RESULTADO DA COMPARAÇÃO:');
    console.log('=====================================');
    console.log(`✅ Nomes ENCONTRADOS no banco: ${nomesEncontrados.length}`);
    console.log(`❌ Nomes NÃO ENCONTRADOS no banco: ${nomesNaoEncontrados.length}`);
    console.log(`📋 Total de nomes verificados: ${nomesFornecidos.length}`);
    
    console.log('\n✅ NOMES ENCONTRADOS:');
    nomesEncontrados.forEach((nome, index) => {
      console.log(`${index + 1}. ${nome}`);
    });
    
    console.log('\n❌ NOMES NÃO ENCONTRADOS:');
    nomesNaoEncontrados.forEach((nome, index) => {
      console.log(`${index + 1}. ${nome}`);
    });
    
    // Estatísticas
    const percentualEncontrado = ((nomesEncontrados.length / nomesFornecidos.length) * 100).toFixed(1);
    console.log(`\n📈 PERCENTUAL: ${percentualEncontrado}% dos nomes foram encontrados no banco`);
    
    if (nomesNaoEncontrados.length > 0) {
      console.log('\n⚠️  RECOMENDAÇÕES:');
      console.log('- Verificar se os nomes não encontrados foram importados com grafia diferente');
      console.log('- Verificar se houve problemas durante a importação');
      console.log('- Considerar re-importar os clientes não encontrados');
    }
    
  } catch (error) {
    console.error('❌ Erro durante a comparação:', error);
  } finally {
    // Fechar conexão
    await mongoose.connection.close();
    console.log('\n🔌 Conexão com o banco fechada');
  }
}

// Executar comparação
compararNomes();

